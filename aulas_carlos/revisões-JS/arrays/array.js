const array = [21, 6, 84, 24, 86, 11]
// Arrays -> métodos comuns


// Inserir dados
array.push(5) // adiciona coisa no FINAL do array
console.log(array)

array.unshift(5)  // adiciona coisa no COMEÇO no array
console.log(array)


// Remover dados
array.pop() // remove coisa do final, não precisa de parâmetros
console.log(array)

array.shift() // remove coisa no começo, também não precisa de parâmetros
console.log(array)